               <p class="mb-10"> India, since centuries, is a country well known for its Vedic culture, its famous temples, the righteous human beings and above all the deep philosophy of <b>Sanatana Dharma.</b> </p>
               <p class="mb-10"> By Krishna’s grace, even today’s society has a chance to become spiritually situated, while living in a material world and harmonize the broken link between human and God.</p>
               <p class="mb-10"> By the mercy of HDG Srila Prabhupada, the Founder Acharya of the World wide Hare Krishna Movement, today, in India and in many parts of the World, beautiful temples are being built which are bringing together people from various walks of life to <b>Krishna Consciousness.</b></p>
                <p><b>Lord Sri Krishna says in Bhagavad Gīta 9.26: </b>“If one offers me with love and devotion a leaf, a flower, a fruit or water, I will accept it.”</p>
               <p> The Supreme Lord, Sri Krishna, is not in need of anything from us; but when we offer Him something depending on our capacity, He blesses us with everything that we need to progress in achieving the ultimate goal of our life.</p>

               <p> It was a prime duty in the good olden days, to build temples and conduct festivals and enlighten people in Knowledge about God and his activities. The Puranas also glorify the act of temple construction for Krishna in this way: </p>
               <blockquote class="theme-colored pt-20 pb-20">
               <p> <b>According to Vamana Purana:</b> “Whoever constructs or helps to construct a Vishnu temple will protect eight generations of fathers, grandfathers, and forefathers from falling into hell.”</p>
               </blockquote>
               <p> And, “By building a temple for Sri Madhava, one can attain the eternal spiritual world (Vaikuntha).</p>

               <p> <b>Skanda Purana:</b> “Just by starting the construction of a temple for Lord, sins committed in seven births will be wiped out and one will deliver his forefathers who are suffering in hellish planets” and “who sponsors a temple of Lord Vishnu, gains ten million times the merits accrued by Yajna, Austerities, Charities, Pilgrimage and study of Vedas. </p>

              <blockquote class="theme-colored pt-20 pb-20">
               <p><b> Padma Purana:</b> “Mortals who help to construct a temple of Lord Vishnu do not return to this world, they return to the abode of Lord Vishnu”.</p>
              </blockquote>

               <p><b> Srimad Bhagavatam 11.27.51:</b> Lord Krishna tells Uddhava: One who offers the Deity gifts of land, markets, cities and villages so that the regular daily worship and special festivals of the Deity may go on continually will achieve opulence equal to My own.</p>

                <p><b>Srimad Bhagavatam 11.27.52-53:</b> “By installing the Deity of the Lord, one becomes king of the entire earth, by building a temple for the Lord, one becomes ruler of the three worlds, by worshiping and serving the deity, one goes to the planet of Lord Brahma, and by performing all three of these activities one achieves a transcendental form like My own. But one who simply engages in devotional service with no consideration of fruitive results attains Me. Thus whoever worships Me according to the process I have described will ultimately attain pure devotional service unto Me.”</p>


                
                <p>With love and affection, one should try to give something to Krishna from one’s source of income. Then one’s life will be successful. Krishna is full in all opulences; He does not need anything from anyone. But if one is prepared to give something to Krishna, that is for one’s own benefit.</p>
